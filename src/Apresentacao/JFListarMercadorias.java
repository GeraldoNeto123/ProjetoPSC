    private void SelecionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SelecionarActionPerformed
        if (jTProdutos.getSelectedRow() != -1) {
            Mercadoria m = new Mercadoria();
            m = MercadoriaDAO.getInstance().getById((int)jTProdutos.getValueAt(jTProdutos.getSelectedRow(), 0));
            
            if(FContexto.getNovaVenda().isVisible()){
            if(m.getQuantidade()>=1){
                FContexto.getNovaVenda().setCampo(m);
                dispose();
            }else{ 
                JOptionPane.showMessageDialog(this, "Sem o produto em estoque");
                
            }
            }
            if(FContexto.getMercadoria().isVisible()){
              FContexto.getMercadoria().AlterarRegistro(m);
              dispose();  
            }
            
        }
    }//GEN-LAST:event_SelecionarActionPerformed

