    private void jBEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBEntrarActionPerformed
        UsuarioDAO usuarioDAO = new UsuarioDAO();
        boolean verifica = false;
        String senha = criptografar(jTFSenha.getText());
        for (Usuario u : usuarioDAO.findAll()) {
            if(u.getLogin().equals(jTFLogin.getText())&& u.getSenha().equals(senha)){
                verifica = true;
                break;
            }
        }
        
        if(!verifica){
            JOptionPane.showMessageDialog(this, " Login Inv√°lido!");
            limpar();
        }    
        
        if(verifica){
            FContexto.getHome().setVisible(true);
            this.dispose();
             limpar();
        }
        limpar();
    }//GEN-LAST:event_jBEntrarActionPerformed

