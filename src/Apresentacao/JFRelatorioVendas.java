     public void readTable(){
    DefaultTableModel modelo = (DefaultTableModel) jTVendas.getModel();
        int size = modelo.getRowCount();
        
        ((DefaultTableModel) jTVendas.getModel()).setNumRows(0);
        jTVendas.updateUI();
        
        VendaDAO venda = new VendaDAO();
        SimpleDateFormat formatter = new SimpleDateFormat("dd/MM/yyyy");
        String data;

            
        for (Venda ven : venda.findAll()) {
            data = formatter.format(ven.getData());
            modelo.addRow(new Object[]{
                ven.getCodigo(),
                data,
                ven.getValorTotal()
            });
            
        }
    }

